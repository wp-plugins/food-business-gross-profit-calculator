			<h3>Installation</h3>
			<ol>
				<li>BACKUP everything before you install the plugin.</li>
				<li>Upload food-business-gross-profit-calculator directory to the `/wp-content/plugins/` directory</li>
				<li>Activate the plugin through the 'Plugins' menu in WordPress</li>	
				<li>Update the Permalinks on settings.</li>
				<li>SCRecipe post type should now available at dashboard and start creating recipe post type.</li>
			</ol>
			
			<h3>Basic Instruction on Creating a Recipe Post types</h3>

			<ol>
				<li>Create Recipe Post Type</li>
				<li>You may add Recipe categories for your recipe.</li>
				<li>Add contents for your recipe with images, description, ingredients.</li>
				<li>Publish the recipe post.</li>
				<li>View the recipe at the frontend using the button View Recipe.</li>
				<li>Food calculator form should be visible at the bottom of your recipe and you can start adding the ingredients with their measurements and prices.</li>
				<li>Add the total items or servings that is produced with the given measurements and prices.</li>
				<li>You can update the currency by setting the Currency.</li>
				<li>Set the Gross Profit Percentage</li>
				<li>Add your pressumed operating expenses.</li>
				<li>Click Calculate button and summary details should be updated with the price per serving/item shown.</li>
			</ol>
			
		
